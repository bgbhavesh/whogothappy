app.intermediate.get = function(){
	var truePosition = app.randomNumber(0,6);
    var images = []
    for(var i=0,il=16;i<il;i++){
    if(i == truePosition)
      images.push({"image": app[app.gameLevel].image[app.randomNumber(0,6)][app.randomNumber(0,15)],
      	"winner": true
      	});
    else
        images.push({"image": app[app.gameLevel].image[truePosition][i],
        	"winner": false
      	});
  	}
  	return images;
}

